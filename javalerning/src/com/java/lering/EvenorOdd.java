package com.java.lering;

public class EvenorOdd {

	public static void main(String[] args) {
		
		
		
		for(int i=1;i<=10;i++)
		{
			if(i%2==0)
			{
				System.out.println(i+"the number is even");
				
			}
			else
			{
				System.out.println(i+"the number is odd");
			}
		}
		

	}

}
